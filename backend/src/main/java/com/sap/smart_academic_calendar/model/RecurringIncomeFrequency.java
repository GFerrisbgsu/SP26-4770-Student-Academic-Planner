package com.sap.smart_academic_calendar.model;

/**
 * Enum representing the frequency of recurring income.
 * Used to determine how often an income transaction should automatically be created.
 */
public enum RecurringIncomeFrequency {
    WEEKLY,      // Every 7 days
    BIWEEKLY,    // Every 14 days
    MONTHLY      // Same day each month
}
